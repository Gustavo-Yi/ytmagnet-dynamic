const getImageFilename = (imageUrl) => {
    const cleanPath = String(imageUrl ?? "").split(/[?#]/)[0];
    return cleanPath.split("/").pop();
};

export const hasGeneratedFerriteTexture = (item) =>
    item?.material === "ferrite" &&
    GENERATED_FERRITE_TEXTURE_FILENAMES.has(getImageFilename(item.image_url));
